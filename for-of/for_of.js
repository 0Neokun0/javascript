// Array
const names = ['Vishu', 'Bhrama', 'Mahesh'];
// Using for..of
for ( let element of names ) {
    console.log(element);
}

// String
const string = 'SHIVA';
// Using for..of loop
for (let i of string) {
    console.log(i);
}

// Define Set
const set = new Set([1, 2, 3]);

// Looping through Set
for (let i of set) {
    console.log(i);
}