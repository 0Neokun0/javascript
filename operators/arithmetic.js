// Variables

let number1 = 5;
let number2 = 3;

// Addition
console.log(`${number1} + ${number2} = ${number1 + number2}`);

// Subtraction
console.log(`${number1} - ${number2} = ${number1 - number2}`);

// Multiplication
console.log(`${number1} * ${number2} = ${number1 * number2}`);

// Division
console.log(`${number1} / ${number2} = ${number1 / number2}`);

// Remainder
console.log(`${number1} % ${number2} = ${number1 % number2}`);

// Exponentiation
console.log(`${number1} ** ${number2} = ${number1 ** number2}`);

